#include <stdio.h>

void printKamisadoGrid() {
    char grid[8][8] = {
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
        {' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '},
    };

    printf("\n");

    for (int i = 0; i < 8; i++) {
        for (int j = 0; j < 8; j++) {
            if (j == i) {
                printf("\x1b[40m");
            }
            else if ((i == 0 && j == 1) || (i == 1 && j == 4) || (i == 2 && j == 7) || (i == 3 && j == 2) || (i == 4 && j == 5) || (i == 5 && j == 0) || (i == 6 && j == 3) || (i == 7 && j == 6)) {
                printf("\x1b[41m");
            }
            else if ((i == 0 && j == 2) || (i == 1 && j == 7) || (i == 2 && j == 4) || (i == 3 && j == 1) || (i == 4 && j == 6) || (i == 5 && j == 3) || (i == 6 && j == 0) || (i == 7 && j == 5)) {
                printf("\x1b[42m");
            }
            else if ((i == 0 && j == 3) || (i == 1 && j == 2) || (i == 2 && j == 1) || (i == 3 && j == 0) || (i == 4 && j == 7) || (i == 5 && j == 6) || (i == 6 && j == 5) || (i == 7 && j == 4)) {
                printf("\x1b[43m");
            }
            else if ((i == 0 && j == 4) || (i == 1 && j == 5) || (i == 2 && j == 6) || (i == 3 && j == 7) || (i == 4 && j == 0) || (i == 5 && j == 1) || (i == 6 && j == 2) || (i == 7 && j == 3)) {
                printf("\x1b[44m");
            }
            else if ((i == 0 && j == 5) || (i == 1 && j == 0) || (i == 2 && j == 3) || (i == 3 && j == 6) || (i == 4 && j == 1) || (i == 5 && j == 4) || (i == 6 && j == 7) || (i == 7 && j == 2)) {
                printf("\x1b[45m");
            }
            else if ((i == 0 && j == 6) || (i == 1 && j == 3) || (i == 2 && j == 0) || (i == 3 && j == 5) || (i == 4 && j == 2) || (i == 5 && j == 7) || (i == 6 && j == 4) || (i == 7 && j == 1)) {
                printf("\x1b[46m");
            }
            else {
                printf("\x1b[47m");
            }

            printf("%c ", grid[i][j]);
        }
        printf("\n");
    }

    printf("\n");
}
